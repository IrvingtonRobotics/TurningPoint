int deDead(int speed) {
	// deal with dead zone
	if (abs(speed) < 10) {
		return 0;
	}
	return speed;
}

void moveDrive(int leftPow, int rightPow) {
	// Move the left side of the robot
	motor[driveLeftFront] = leftPow;
	motor[driveLeftBack] = leftPow;
	// Move the right side of the robot
	motor[driveRightFront] = rightPow;
	motor[driveRightBack] = rightPow;
}

void doDrive() {
	int y = deDead(vexRT[Ch3]);
	int x = deDead(vexRT[Ch4]);
	int leftSpeed = x + y;
	int rightSpeed = x - y;
	moveDrive(leftSpeed, rightSpeed);
}

void moveArm(int dir) {
	motor[liftTop] = dir;
	motor[liftBottom] = dir;
}

void runConveyor(int speed) {
	motor[conveyor] = speed;
}

void doConveyor() {
	if (vexRT[Btn6U]) {
		runConveyor(127);
	} else if (vexRT[Btn6D]) {
		runConveyor(-127);
	} else {
		runConveyor(0);
	}
}

//bool flywheelGoing = false;

void runFlywheel(int speed) {
	motor[flywheelLeft] = speed;
	motor[flywheelRight] = speed;
}

void doFlywheel() {
	if (vexRT[Btn5D]) {
		runFlywheel(127);
	} else {
		runFlywheel(0);
	}
}

void doArms() {
	int speed = deDead(vexRT[Ch2]);
	moveArm(speed);
}

bool clawFlipped = true;

void turnClaw(int pos) {
	motor[clawFlip] = pos;
}

void flipClaw() {
  turnClaw(clawFlipped ? 50 : -50);
	clawFlipped = !clawFlipped;
}

void doClaw() {
	if (vexRT[Btn5U]) {
		flipClaw();
	}
}

task main() {
	while (true) {
		doDrive();
		doArms();
		doClaw();
		doConveyor();
		doFlywheel();
	}
}
